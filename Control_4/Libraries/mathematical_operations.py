def add (a, b):
    """
    This function can help you to calculate *sum* of two numbers

    * Example:
      - a = 2 
      - b = 4 
      - result 2 + 4 = 6
    """
    return a + b

def subtract (a, b):
    """
    This function can help you to *subtract* two numbers

     * Example:
      - a = 4 
      - b = 2 
      - result 4 - 2 = 2
    """
    return  a - b

def divide (a, b):
    """
    This function can help you to *divide* two numbers

    * Example:
      - a = 4 
      - b = 2 
      - result 4 / 2 = 2

    """
    return  a / b

def multiply (a, b):
    """
    This function can help you to *multiply* two numbers
    
    * Example:
      - a = 2 
      - b = 4 
      - result 2 * 4 = 8
    """
    return a * b
