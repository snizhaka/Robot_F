def add (a, b):
    """
    This function can help you to calculate <b>sum</b> of two numbers
    <ul>
        <li>a = 2,
        <li>b = 4,
        <li>result 2 + 4 = 6.
    </ul>
    """
    return a + b

def subtract (a, b):
    """
    This function can help you to <b>subtract</b> two numbers
    <ul>
        <li>a = 4,
        <li>b = 2,
        <li>result 4 - 2 = 2.
    </ul>
    """
    return  a - b

def divide (a, b):
    """
    This function can help you to <b>divide</b> two numbers
    <ul>
        <li>a = 4,
        <li>b = 2,
        <li>result 4 / 2 = 2.
    </ul>
    """
    return  a / b

def multiply (a, b):
    """
    This function can help you to <b>multiply</b> two numbers
    <ul>
        <li>a = 4,
        <li>b = 2,
        <li>result 4 * 2 = 8.
    </ul>
    """
    return a * b
